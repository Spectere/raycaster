/* palette.h */

#ifndef _PALETTE_H
#define _PALETTE_H

#include "video.h"

#define PAL(idx) RGB(palette_cga[3 * idx],\
                     palette_cga[3 * idx + 1],\
                     palette_cga[3 * idx + 2])

#define PAL_ALT(idx) RGB((int)(palette_cga[3 * idx] / 1.5),\
                         (int)(palette_cga[3 * idx + 1] / 1.5),\
                         (int)(palette_cga[3 * idx + 2] / 1.5))

static const Uint8 palette_cga[48] = {
	0x00, 0x00, 0x00,	/* 0x00 */
	0x00, 0x00, 0xAA,	/* 0x01 */
	0x00, 0xAA, 0x00,	/* 0x02 */
	0x00, 0xAA, 0xAA,	/* 0x03 */
	0xAA, 0x00, 0x00,	/* 0x04 */
	0xAA, 0x00, 0xAA,	/* 0x05 */
	0xAA, 0x55, 0x00,	/* 0x06 */
	0xAA, 0xAA, 0xAA,	/* 0x07 */
	0x55, 0x55, 0x55,	/* 0x08 */
	0x55, 0x55, 0xFF,	/* 0x09 */
	0x55, 0xFF, 0x55,	/* 0x0A */
	0x55, 0xFF, 0xFF,	/* 0x0B */
	0xFF, 0x55, 0x55,	/* 0x0C */
	0xFF, 0x55, 0xFF,	/* 0x0D */
	0xFF, 0xFF, 0x55,	/* 0x0E */
	0xFF, 0xFF, 0xFF	/* 0x0F */
};

#endif /* _PALETTE_H */
